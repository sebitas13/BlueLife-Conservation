    private void tbHabitatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbHabitatMouseClicked
        int seleccion = tbHabitat.rowAtPoint(evt.getPoint());
        try {
          
            System.out.println(Integer.parseInt((String)(tbHabitat.getValueAt(seleccion, 0))));
            HabitatDTO ha = habitatController.buscarHabitat(Integer.parseInt((String)(tbHabitat.getValueAt(seleccion, 0))));
            this.txtIdhabitat.setText(String.valueOf(ha.getIdhabitat()));
            this.txtNombre.setText(String.valueOf(ha.getNombre()));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "NO EXISTE EL HABITAT");
        }
    }//GEN-LAST:event_tbHabitatMouseClicked

    

