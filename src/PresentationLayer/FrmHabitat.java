    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int idhabitat = 0;
        idhabitat = Integer.parseInt(this.txtIdhabitat.getText());

        HabitatDTO ha = habitatController.buscarHabitat(idhabitat);

        if(ha.getIdhabitat() != 0){
            this.txtIdhabitat.setText(String.valueOf(ha.getIdhabitat()));
            this.txtNombre.setText(String.valueOf(ha.getNombre()));

        }
        else{
            JOptionPane.showMessageDialog(null, "No existe el habitat");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

