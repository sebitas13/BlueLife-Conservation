    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        VentaDTO venta = new VentaDTO();
        int numerocaptura = 0;
        long ruc = 0;
        int idespecie = 0;
        int idmercado = 0;
        float cantidadvendida = 0;

        numerocaptura = Integer.valueOf(this.txtNumerocaptura.getText());
        ruc = Long.valueOf(this.txtRuc.getText());
        idespecie = Integer.valueOf(this.txtIdespecie.getText());
        idmercado = Integer.valueOf(this.txtIdmercado.getText());
        cantidadvendida = Float.valueOf(this.txtCantidadvendida.getText());

        venta.setNumerocaptura(numerocaptura);
        venta.setRuc(ruc);
        venta.setIdespecie(idespecie);
        venta.setIdmercado(idmercado);
        venta.setCantidadvendida(cantidadvendida);

        String mensaje = ventaController.agregarVenta(venta);
        JOptionPane.showMessageDialog(null, mensaje);
        listarVenta();
    }//GEN-LAST:event_btnAgregarActionPerformed

