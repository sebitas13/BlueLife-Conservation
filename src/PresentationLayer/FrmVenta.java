    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int ncaptura = 0;
        ncaptura = Integer.parseInt(this.txtNumerocaptura.getText());

      
        VentaDTO ven = ventaController.buscarVentaPorNumeroCaptura(ncaptura);

        if(ven.getIdventa() != 0){
            this.txtIdventa.setText(String.valueOf(ven.getIdventa()));
            this.txtNumerocaptura.setText(String.valueOf(ven.getNumerocaptura()));
            this.txtRuc.setText(String.valueOf(ven.getRuc()));
            this.txtIdespecie.setText(String.valueOf(ven.getIdespecie()));
            this.txtIdmercado.setText(String.valueOf(ven.getIdmercado()));
            this.txtCantidadvendida.setText(String.valueOf(ven.getCantidadvendida()));
        }
        else{
            JOptionPane.showMessageDialog(null, "No existe la venta");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void tbVentaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbVentaMouseClicked
        int seleccion = tbVenta.rowAtPoint(evt.getPoint());
        try {
           
            System.out.println(Integer.parseInt((String)(tbVenta.getValueAt(seleccion, 0))));
            VentaDTO ve = ventaController.buscarVenta(Integer.parseInt((String)(tbVenta.getValueAt(seleccion, 0))));
            this.txtIdventa.setText(String.valueOf(ve.getIdventa()));
            this.txtNumerocaptura.setText(String.valueOf(ve.getNumerocaptura()));
            this.txtRuc.setText(String.valueOf(ve.getRuc()));
            this.txtIdespecie.setText(String.valueOf(ve.getIdespecie()));
            this.txtIdmercado.setText(String.valueOf(ve.getIdmercado()));
            this.txtCantidadvendida.setText(String.valueOf(ve.getCantidadvendida()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "NO EXISTE LA VENTA");
        }
    }//GEN-LAST:event_tbVentaMouseClicked

