    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        long ruc = 0;
        ruc = Long.parseLong(this.txtRuc.getText());

       
        EmpresaDTO em = empresaController.buscarEmpresa(ruc);

        if(em.getRuc() != 0){
            this.txtRuc.setText(String.valueOf(em.getRuc()));
            this.txtRazonsocial.setText(String.valueOf(em.getRazonsocial()));
        }
        else{
            JOptionPane.showMessageDialog(null, "No existe la empresa");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    

