    private void tbEmpresaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbEmpresaMouseClicked
        int seleccion = tbEmpresa.rowAtPoint(evt.getPoint());
        try {
            
            System.out.println((String)(tbEmpresa.getValueAt(seleccion, 0)));
            EmpresaDTO em = empresaController.buscarEmpresa(Long.parseLong((String)(tbEmpresa.getValueAt(seleccion, 0))));
            this.txtRuc.setText(String.valueOf(em.getRuc()));
            this.txtRazonsocial.setText(em.getRazonsocial());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "NO EXISTE LA EMPRESA");
        }
    }//GEN-LAST:event_tbEmpresaMouseClicked

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        EmpresaDTO empresa = new EmpresaDTO();
        long ruc = 0;
        String razonsocial = " ";

        ruc = Long.valueOf(this.txtRuc.getText());
        razonsocial = String.valueOf(this.txtRazonsocial.getText());

        empresa.setRuc(ruc);
        empresa.setRazonsocial(razonsocial);

        
        String mensaje = empresaController.agregarEmpresa(empresa);
        JOptionPane.showMessageDialog(null, mensaje);
        listarEmpresa();
    }//GEN-LAST:event_btnAgregarActionPerformed

