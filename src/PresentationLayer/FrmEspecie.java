    private void tbEspecieMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbEspecieMouseClicked
        int seleccion = tbEspecie.rowAtPoint(evt.getPoint());
        try {
            
            System.out.println(Integer.parseInt((String)(tbEspecie.getValueAt(seleccion, 0))));
            EspecieDTO es = especieController.buscarEspecie(Integer.parseInt((String)(tbEspecie.getValueAt(seleccion, 0))));
            this.txtIdespecie.setText(String.valueOf(es.getIdespecie()));
            this.txtIdcategoria.setText(String.valueOf(es.getIdcategoria()));
            this.txtIdhabitat.setText(String.valueOf(es.getIdhabitat()));
            this.txtNombre.setText(String.valueOf(es.getNombre()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "NO EXISTE LA ESPECIE");
        }
    }//GEN-LAST:event_tbEspecieMouseClicked

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        EspecieDTO especie = new EspecieDTO();
        int idespecie = 0;
        int idcategoria = 0;
        int idhabitat = 0;
        String nombre = " ";

        String cadena1 = (String) cboIDhabitatMod.getSelectedItem();
        String[] partes1 = cadena1.split("-");
        String id_habitat = partes1[0];

        String cadena2 = (String) cboIDcategoriaMod.getSelectedItem();
        String[] partes2 = cadena2.split("-");
        String id_categoria = partes2[0];

        idespecie = Integer.valueOf(idespecie);
        idcategoria = Integer.valueOf(id_categoria);
        idhabitat = Integer.valueOf(id_habitat);
        nombre = String.valueOf(this.txtNombreEspecieMod.getText());

        especie.setIdespecie(idespecie);
        especie.setIdcategoria(idcategoria);
        especie.setIdhabitat(idhabitat);
        especie.setNombre(nombre);

     
        String mensaje = especieController.agregarEspecie(especie);
        JOptionPane.showMessageDialog(null, mensaje);
        listarEspecie();

       
    }//GEN-LAST:event_btnAgregarActionPerformed

