    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int idespecie = 0;
        //  idespecie = Integer.parseInt(this.txtIdespecie.getText());
        idespecie = Integer.parseInt(this.txtBuscarEspecie.getText());

   
        EspecieDTO esp = especieController.buscarEspecie(idespecie);

        if(esp.getIdespecie() != 0){
            this.txtIdespecie.setText(String.valueOf(esp.getIdespecie()));
            this.txtIdcategoria.setText(String.valueOf(esp.getIdcategoria()));
            this.txtIdhabitat.setText(String.valueOf(esp.getIdhabitat()));
            this.txtNombre.setText(String.valueOf(esp.getNombre()));
        }
        else{
            JOptionPane.showMessageDialog(null, "No existe la captura");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

