    private void tbCategoriaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbCategoriaMouseClicked
        int seleccion = tbCategoria.rowAtPoint(evt.getPoint());
        try {
           
            System.out.println(Integer.parseInt((String)(tbCategoria.getValueAt(seleccion, 0))));
            CategoriaDTO ca = categoriaController.buscarCategoria(Integer.parseInt((String)(tbCategoria.getValueAt(seleccion, 0))));
            this.txtIdCategoria.setText(String.valueOf(ca.getIdcategoria()));
            this.txtNombre.setText(String.valueOf(ca.getNombre()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "NO EXISTE LA CATEGORIA");
        }
    }//GEN-LAST:event_tbCategoriaMouseClicked

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int idcategoria = 0;
        idcategoria = Integer.parseInt(this.txtIdCategoria.getText());

        CategoriaDTO cat = categoriaController.buscarCategoria(idcategoria);

        if(cat.getIdcategoria() != 0){
            this.txtIdCategoria.setText(String.valueOf(cat.getIdcategoria()));
            this.txtNombre.setText(String.valueOf(cat.getNombre()));
        }
        else{
            JOptionPane.showMessageDialog(null, "No existe la categoria");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

