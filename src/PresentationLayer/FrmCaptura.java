    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        long ruc = 0;
        int idespecie = 0;
        Date fecha;
        float cantidadcaptura = 0;

        String cadena1 = (String) cboEspecie.getSelectedItem();
        String[] partes1 = cadena1.split("-");
        String id_especie = partes1[0];

        String cadena2 = (String) cboRUC.getSelectedItem();
        String[] partes2= cadena2.split("-");
        String id_RUC = partes2[0];

        ruc = Long.valueOf(id_RUC);
        System.out.println("RUCC: "+ruc);
        idespecie = Integer.valueOf(id_especie);
        fecha = this.jDate.getDate();
        cantidadcaptura = Float.valueOf(this.txtCantidadMod.getText());

        String mensaje = capturaController.agregarCaptura(ruc, idespecie, fecha, cantidadcaptura);
        JOptionPane.showMessageDialog(null, mensaje);

      
        listarCaptura();

    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
     
        
       // CapturaVO captura = new CapturaVO();
        int numerocaptura = 0;
        long ruc = 0;
        int idespecie = 0;
        Date fecha;
        float cantidadcaptura = 0;

        String cadena1 = (String) cboRUC.getSelectedItem();
        String[] partes1 = cadena1.split("-");
        String id_RUC = partes1[0];

        String cadena2 = (String) cboEspecie.getSelectedItem();
        String[] partes2= cadena2.split("-");
        String id_especie = partes2[0];

        numerocaptura = Integer.parseInt(this.txtIdCaptura.getText());
        ruc = Long.valueOf(id_RUC);
        idespecie = Integer.valueOf(id_especie);
        fecha = this.jDate.getDate();
        cantidadcaptura = Float.valueOf(this.txtCantidadMod.getText());

        String mensaje =  capturaController.modificarCaptura(numerocaptura, ruc, idespecie, fecha, cantidadcaptura);
        JOptionPane.showMessageDialog(null, mensaje);
        listarCaptura();
        
    }//GEN-LAST:event_btnActualizarActionPerformed

