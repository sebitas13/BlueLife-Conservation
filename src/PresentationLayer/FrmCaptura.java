    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        int numerocaptura = 0;
        numerocaptura = Integer.parseInt(this.txtbuscar.getText());

      //  CapturaBO capBO = new CapturaBO();
        CapturaDTO cap = capturaController.buscarCaptura(numerocaptura);
        
        
        if(cap.getNumerocaptura() != 0){
            this.txtRuc.setText(String.valueOf(cap.getRuc()));
            this.txtIdEspecie.setText(String.valueOf(cap.getIdespecie()));
            this.jdcFecha.setDate(cap.getFecha());
            this.txtCantidad.setText(String.valueOf(cap.getCantidadporcaptura()));
        }
        else{
            JOptionPane.showMessageDialog(null, "No existe la captura");
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

    private void tbCapturaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbCapturaMouseClicked
        int seleccion = tbCaptura.rowAtPoint(evt.getPoint());
        try {
          //  CapturaBO capBO = new CapturaBO();
            System.out.println(Integer.parseInt((String)(tbCaptura.getValueAt(seleccion, 0))));
            CapturaDTO c = capturaController.buscarCaptura(Integer.parseInt((String)(tbCaptura.getValueAt(seleccion, 0))));
            this.txtIdCaptura.setText(String.valueOf(c.getNumerocaptura()));
            this.txtRuc.setText(String.valueOf(c.getRuc()));
            this.txtIdEspecie.setText(String.valueOf(c.getIdespecie()));
            this.jdcFecha.setDate(c.getFecha());
            this.txtCantidad.setText(String.valueOf(c.getCantidadporcaptura()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "NO EXISTE LA CAPTURA");
        }

    }//GEN-LAST:event_tbCapturaMouseClicked

