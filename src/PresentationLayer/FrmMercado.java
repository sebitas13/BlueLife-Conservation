    private void tbMercadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbMercadoMouseClicked
        int seleccion = tbMercado.rowAtPoint(evt.getPoint());
        try {
          
            System.out.println(Integer.parseInt((String)(tbMercado.getValueAt(seleccion, 0))));
            MercadoDTO mer = mercadoController.buscarMercado(Integer.parseInt((String)(tbMercado.getValueAt(seleccion, 0))));
            this.txtIdmercado.setText(String.valueOf(mer.getMercado()));
            this.txtIdtipomercado.setText(String.valueOf(mer.getTipomercado()));
            this.txtNombre.setText(String.valueOf(mer.getNombremercado()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "NO EXISTE EL MERCADO");
        }
    }//GEN-LAST:event_tbMercadoMouseClicked

    

