    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int idmercado = 0;
        idmercado = Integer.parseInt(this.txtIdmercado.getText());

        MercadoDTO mer = mercadoController.buscarMercado(idmercado);

        if(mer.getMercado() != 0){
            this.txtIdmercado.setText(String.valueOf(mer.getMercado()));
            this.txtIdtipomercado.setText(String.valueOf(mer.getTipomercado()));
            this.txtNombre.setText(String.valueOf(mer.getNombremercado()));
        }
        else{
            JOptionPane.showMessageDialog(null, "No existe el mercado");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

