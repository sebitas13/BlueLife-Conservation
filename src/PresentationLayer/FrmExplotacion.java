    private void tbExplotacionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbExplotacionMouseClicked
        int seleccion = tbExplotacion.rowAtPoint(evt.getPoint());
        try {
            
            System.out.println(Integer.parseInt((String)(tbExplotacion.getValueAt(seleccion, 0))));
            ExplotacionDTO ex = explotacionController.buscarExplotacion(Integer.parseInt((String)(tbExplotacion.getValueAt(seleccion, 0))));
            this.txtIdespecie.setText(String.valueOf(ex.getIdespecie()));
            this.txtIdestado.setText(String.valueOf(ex.getIdestado()));
            this.txtCantidadacumulada.setText(String.valueOf(ex.getCantidadacumulada()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "NO EXISTE LA EXPLOTACION SOLICITADA");
        }
    }//GEN-LAST:event_tbExplotacionMouseClicked

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int idespecie = 0;
        idespecie = Integer.parseInt(this.txtIdespecie.getText());

      
        ExplotacionDTO ex = explotacionController.buscarExplotacion(idespecie);

        if(ex.getIdespecie() != 0){
            this.txtIdespecie.setText(String.valueOf(ex.getIdespecie()));
            this.txtIdestado.setText(String.valueOf(ex.getIdestado()));
            this.txtCantidadacumulada.setText(String.valueOf(ex.getCantidadacumulada()));
        }
        else{
            JOptionPane.showMessageDialog(null, "No existe la explotacion");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

