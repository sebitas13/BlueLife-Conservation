    private void txtIdespecieKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIdespecieKeyPressed
        if(evt.getKeyCode()== KeyEvent.VK_ENTER){
            EspecieDTO espe = new EspecieDTO();
            ExplotacionDTO ex = new ExplotacionDTO();
            CategoriaDTO ca = new CategoriaDTO();
            HabitatDTO ha = new HabitatDTO();
            EstadoDTO esta = new EstadoDTO();
            
           
            espe = especieController.buscarEspecie(Integer.parseInt(this.txtIdespecie.getText()));
            ex = explotacionController.buscarExplotacion(Integer.parseInt(this.txtIdespecie.getText()));
            ca = categoriaController.buscarCategoria(espe.getIdcategoria());
            ha = habitatController.buscarHabitat(espe.getIdhabitat());
            esta = estadoController.buscarEstado(ex.getIdestado());
            this.txtEspecie.setText(espe.getNombre());
            this.txtCategoria.setText(ca.getNombre());
            this.txtHabitat.setText(ha.getNombre());
            this.txtCantidad.setText(String.valueOf(ex.getCantidadacumulada()));
            this.txtEstado.setText(esta.getNombre());
            capturaController.listarCapturas(tbCaptura,espe.getIdespecie());
        }
    }//GEN-LAST:event_txtIdespecieKeyPressed

    

